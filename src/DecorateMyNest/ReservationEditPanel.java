        private void insertbtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_insertbtnActionPerformed
                // TODO add your handling code here:
                if (clientID.getText().equals("") || catalogueID.getText().equals("") || Date.getText().equals("")
                                || Month.getText().equals("") || Year.getText().equals("")) {
                        JOptionPane.showMessageDialog(this, "Please Fill-up all fields");
                        return;
                }
                try {
                        Welcome.jdbc.reservationInsert(Integer.parseInt(clientID.getText()),
                                        Integer.parseInt(catalogueID.getText()),
                                        new Date(Integer.parseInt(Year.getText()),
                                                        Integer.parseInt(Month.getText()),
                                                        Integer.parseInt(Date.getText())));
                        JOptionPane.showMessageDialog(this, "Inserted Successfully");
                        // Clear your input fields related to Reservation here
                        clientID.setText("");
                        catalogueID.setText("");
                        Date.setText("");
                        Month.setText("");
                        Year.setText("");
                } catch (Exception e) {
                        System.out.println(e);
                        return;
                }

        }// GEN-LAST:event_insertbtnActionPerformed

