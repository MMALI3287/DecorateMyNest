        private void insertbtn1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_insertbtn1ActionPerformed
                if (adminID.getText().equals("") || Date.getText().equals("") || Month.getText().equals("")
                                || Year.getText().equals("") || amount.getText().equals("")
                                || description.getText().equals("")) {
                        JOptionPane.showMessageDialog(this, "Please Fill-up all fields");
                        return;
                }
                try {
                        Welcome.jdbc.financialTransInsert(Integer.parseInt(adminID.getText()),
                                        new Date(Integer.parseInt(Year.getText()), Integer.parseInt(Month.getText()),
                                                        Integer.parseInt(Date.getText())),
                                        Double.parseDouble(amount.getText()), description.getText());
                } catch (Exception e) {
                        System.out.println(e);
                        return;
                }

                if (materialID.getText().equals("") || quantity.getText().equals("") || vendorID.getText().equals("")) {
                        JOptionPane.showMessageDialog(this, "Please Fill-up all fields");
                        return;
                }
                try {
                        int highestTransactionID = Welcome.jdbc.getHighestTransactionID();

                        Welcome.jdbc.materialTransactionInsert(highestTransactionID,
                                        Integer.parseInt(materialID.getText()),
                                        Integer.parseInt(quantity.getText()), Integer.parseInt(vendorID.getText()));
                        JOptionPane.showMessageDialog(this, "Inserted Successfully");
                        // Clear your input fields related to MaterialTransaction here
                        materialID.setText("");
                        quantity.setText("");
                        vendorID.setText("");
                        adminID.setText("");
                        Date.setText("");
                        Month.setText("");
                        Year.setText("");
                        amount.setText("");
                        description.setText("");
                } catch (Exception e) {
                        System.out.println(e);
                        return;
                }

        }// GEN-LAST:event_insertbtn1ActionPerformed

