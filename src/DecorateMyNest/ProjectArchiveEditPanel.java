    private void insertbtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_insertbtnActionPerformed
        // TODO add your handling code here:
        if (projectID.getText().equals("") || Date.getText().equals("") || Month.getText().equals("")
                || Year.getText().equals("") || review.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Fill-up all fields");
            return;
        }
        try {
            Welcome.jdbc
                    .projectArchiveInsert(
                            Integer.parseInt(projectID.getText()),
                            new Date(Integer.parseInt(Year.getText()) - 1900,
                                    Integer.parseInt(Month.getText()) - 1,
                                    Integer.parseInt(Date.getText())),
                            review.getText());
            JOptionPane.showMessageDialog(this, "Inserted Successfully");
            // Clear your input fields related to ProjectArchive here
            projectID.setText("");
            Date.setText("");
            Month.setText("");
            Year.setText("");
            review.setText("");
        } catch (Exception e) {
            System.out.println(e);
            return;
        }

    }// GEN-LAST:event_insertbtnActionPerformed

