    private void okbtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_okbtnActionPerformed
        // TODO add your handling code here:
        {
            String selectedColumn = (String) jComboBoxselectcolumn.getSelectedItem();
            String searchTerm = jTextField1.getText();

            try {
                String query = "SELECT * FROM Vendors WHERE " + selectedColumn + " LIKE ?";
                Welcome.jdbc.preparedStatement = Welcome.jdbc.connection.prepareStatement(query);
                Welcome.jdbc.preparedStatement.setString(1, "%" + searchTerm + "%");

                Welcome.jdbc.resultSet = Welcome.jdbc.preparedStatement.executeQuery();
                model.setRowCount(0); // Clear existing data

                while (Welcome.jdbc.resultSet.next()) {
                    int vendorID = Welcome.jdbc.resultSet.getInt("VendorID");
                    String vendorName = Welcome.jdbc.resultSet.getString("VendorName");
                    String contactPerson = Welcome.jdbc.resultSet.getString("ContactPerson");
                    String phone = Welcome.jdbc.resultSet.getString("Phone");
                    String email = Welcome.jdbc.resultSet.getString("Email");

                    model.addRow(new Object[] { vendorID, vendorName, contactPerson, phone, email });
                }

                // Update the table model with search results
                vendorsTable.setModel(model);
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }

    }// GEN-LAST:event_okbtnActionPerformed

    private void dltbtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_dltbtnActionPerformed
        // TODO add your handling code here:
        {
            int selectedRowIndex = vendorsTable.getSelectedRow();

            if (selectedRowIndex == -1) {
                JOptionPane.showMessageDialog(this, "Please select a row to delete.");
                return;
            }

            int id = (int) vendorsTable.getValueAt(selectedRowIndex, 0);

            int rowsAffected = -1;

            try {
                String query = "DELETE FROM Vendors WHERE VendorID = ?";
                Welcome.jdbc.preparedStatement = Welcome.jdbc.connection.prepareStatement(query);
                Welcome.jdbc.preparedStatement.setInt(1, id);

                rowsAffected = Welcome.jdbc.preparedStatement.executeUpdate();

            } catch (SQLException ex) {
                System.out.println(ex);
            }

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Row deleted successfully.");

                DefaultTableModel model = (DefaultTableModel) vendorsTable.getModel();
                model.removeRow(selectedRowIndex);
            } else {
                JOptionPane.showMessageDialog(this, "Error deleting row.");
            }
        }

    }// GEN-LAST:event_dltbtnActionPerformed

